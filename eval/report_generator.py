"""
report_generator.py

Phase 8 script to automatically populate benchmark_report.md with actual
evaluation metrics from trained models.
"""

import os
import datetime

def generate_report(metrics_dict, output_path):
    """
    metrics_dict should contain:
    - bilstm_top1, bilstm_top5, bilstm_time, bilstm_lat
    - trans_top1, trans_top5, trans_time, trans_lat
    - ctc_ser
    - bleu_score
    - lat_capture, lat_extract, lat_net, lat_infer
    """
    
    report_content = f"""# Ishaara: Benchmark and Evaluation Report

Generated on: {datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

This report documents the actual measured performance of the Ishaara ISL translation system across all pipeline phases.

## 1. Isolated Sign Recognition (Phase 2)

**Model Comparison**
| Architecture | Top-1 Accuracy | Top-5 Accuracy | Training Time (per epoch) | Inference Latency (ms) |
|--------------|----------------|----------------|---------------------------|------------------------|
| BiLSTM       | {metrics_dict.get('bilstm_top1', 'TBD')}%           | {metrics_dict.get('bilstm_top5', 'TBD')}%           | {metrics_dict.get('bilstm_time', 'TBD')}                       | {metrics_dict.get('bilstm_lat', 'TBD')}                    |
| Transformer  | {metrics_dict.get('trans_top1', 'TBD')}%           | {metrics_dict.get('trans_top5', 'TBD')}%           | {metrics_dict.get('trans_time', 'TBD')}                       | {metrics_dict.get('trans_lat', 'TBD')}                    |

## 2. Continuous Signing Segmentation (Phase 4)

**Sign Error Rate (SER)**
- CTC BiLSTM SER: {metrics_dict.get('ctc_ser', 'TBD')}%

> **Co-articulation-naive Limitation:**
> The training and evaluation data for this phase were synthetically generated by concatenating isolated signs. Natural co-articulation (the fluid transition between signs in fluent communication) is entirely absent. The SER reported here likely overestimates the model's capability on natural, conversational ISL.

## 3. Gloss-to-Text Grammar Translation (Phase 5)

**Translation Accuracy**
- BLEU Score: {metrics_dict.get('bleu_score', 'TBD')}

## 4. Live Webcam Demo (Phase 3)

**End-to-End Latency Profile**
- Frame Capture: ~{metrics_dict.get('lat_capture', 'TBD')} ms
- MediaPipe Landmark Extraction: ~{metrics_dict.get('lat_extract', 'TBD')} ms
- Network WebSocket RTT: ~{metrics_dict.get('lat_net', 'TBD')} ms
- Inference (BiLSTM): ~{metrics_dict.get('lat_infer', 'TBD')} ms
- **Total Latency:** ~{sum([metrics_dict.get(k, 0) for k in ['lat_capture', 'lat_extract', 'lat_net', 'lat_infer']])} ms

## 5. Known Limitations

- **Dataset Size Limits:** INCLUDE-50 and INCLUDE are fundamentally small-scale datasets compared to spoken language corpora. Generalization to unseen vocabularies is zero.
- **Signer Diversity & Overfitting:** Even with signer-independent splits, the total pool of 115 signers (in the full dataset) lacks representation of broad regional ISL dialects.
- **Regional Dialect Gaps:** The system is trained on standardized ISL and will likely fail on regional variants.
- **Generative Synthesis Gap:** Text-to-ISL is implemented as a lookup-and-stitch retrieval mechanism, not true generative synthesis, making it brittle to out-of-vocabulary words.
"""
    
    with open(output_path, 'w') as f:
        f.write(report_content)
    print(f"Report successfully generated at {output_path}")

